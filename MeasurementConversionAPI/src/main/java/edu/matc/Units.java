public class Units {
    //Instance Variables
    double teaspoons;
    double tablespoons;
    double cups;
    double quarts;
    double gallons;

    //Getters and Setters
    public double getTeaspoons() {

        return teaspoons;
    }

    public void setTeaspoons(double teaspoons) {

        this.teaspoons = teaspoons;
    }

    public double getTablespoons() {

        return tablespoons;
    }

    public void setTablespoons(double tablespoons) {

        this.tablespoons = tablespoons;
    }

    public double getCups() {

        return cups;
    }

    public void setCups(double cups) {

        this.cups = cups;
    }

    public double getQuarts() {

        return quarts;
    }

    public void setQuarts(double quarts) {

        this.quarts = quarts;
    }

    public double getGallons() {

        return gallons;
    }

    public void setGallons(double gallons) {

        this.gallons = gallons;
    }



}
